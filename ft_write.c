#include "ft_printf.h"

int ft_write(char *c)
{
	char 	*aux;
	int		i;

	aux = ft_strdup(c);
	i = 0;
	while(aux[i] != '\0')
	{
		write(1, &aux[i], 1);
		i++;
	}
	return(0);
}
